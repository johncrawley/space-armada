package com.jacstuff.spacearmada.image;

public class BitmapDimension {

    private int width, height;

    public BitmapDimension(int width, int height){
        this.width = width;
        this.height = height;
    }

    public int getWidth(){
        return this.width;
    }

    public int getHeight(){
        return this.height;
    }
}
